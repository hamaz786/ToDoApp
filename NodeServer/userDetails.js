const mongoose = require('mongoose')

// Schema of Users TodoList Database users data
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
})

mongoose.model('Users', userSchema)
